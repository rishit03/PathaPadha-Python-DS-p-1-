#Program to print the sum of even numbers in range 20 to 30

sum = 0

for i in range(20,30,2):
    sum = sum + i

print("The sum of all even number in range 20 to 30 is : ", sum)



#Program to print the sum of odd numbers in range 20 to 30

sum2 = 0

for j in range(21,30,2):
    sum2 = sum2 + j

print("The sum of all odd number in range 20 to 30 is : ", sum2)
